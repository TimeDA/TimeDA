bool go(ll filled, ll filled1, ll filled2, int i, int j) {
  if (bad.find(make_tuple(filled, filled1, filled2, i, j)) != bad.end()) {
    return false;
  }
  if (filled1 == filled2 && filled1) {
    return true;
  }
  bad.insert({filled, filled1, filled2, i, j});
  if (j == 8) {
    return go(filled, filled1, filled2, i + 1, 0);
  }
  if (i == 8) {
    return false;
  }
  ll bit = (1ull << (i * 8 + j));
  if (!(filled & bit)) {
    if (go(filled, filled1, filled2, i, j + 1)) {
      return true;
    }
    return go(filled | bit, filled1, filled2, i, j);
  }

  if (!(filled1 & bit)) {
    for (int x : first) {
      ll xx;
      if ((xx = put(x, i, j))) {
        if ((filled1 & xx) == 0) {
          if (go(filled | xx, filled1 | xx, filled2, i, j)) {
            to_answer(0, xx);
            return true;
          }
        }
      }
    }
    return false;
  }


  if (!(filled2 & bit)) {
    for (int x : second) {
      ll xx;
      if ((xx = put(x, i, j))) {
        if ((filled2 & xx) == 0) {
          if (go(filled | xx, filled1,  filled2 | xx, i, j)) {
            to_answer(1, xx);
            return true;
          }
        }
      }
    }
    return false;
  }

  return go(filled, filled1, filled2, i, j + 1);
}

