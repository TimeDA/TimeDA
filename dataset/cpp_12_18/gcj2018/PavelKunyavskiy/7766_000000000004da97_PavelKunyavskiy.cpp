void PreCalc() {
  vector<int> v;
  for (int i = 1; i < (1 << 9); i++) {
    int c = 0;
    for (int x = 0; x < 3 && !c; x++) {
      for (int y = 0; y < 3 && !c; y++) {
        if (!(i & (1 << (3 * x + y)))) {
          continue;
        }
        dfs(c, i, x, y);
      }
    }
    if (c == i) {
      v.push_back(normalize(i));
    }
  }
  uniq(v);



  int cnt = 0;
  for (int i : v) {
    for (int j : v) {
      if (i < j) {
        print_mask(i, j);
        fflush(stdout);
        solve(i, j);
        fflush(stdout);
        eprintf("%d %d\n", cnt++, solved);
      }
    }
  }


}


