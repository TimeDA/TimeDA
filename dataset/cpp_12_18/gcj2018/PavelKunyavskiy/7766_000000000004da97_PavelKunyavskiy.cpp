template<typename T>
void uniq(vector<T> &v) {
  sort(v.begin(), v.end());
  v.erase(unique(v.begin(), v.end()), v.end());
}

vector<int> get_all(int m) {
  vector<int> res;

  for (int i = 0; i < 2; i++) {
    for (int j = 0; j < 4; j++) {
      m = rotate(m);
      int mm = normalize_shift(m);
      do {
        int mmm = mm;
        do {
          res.push_back(mmm);
          if ((mmm & ((1 << 2) | (1 << 5) | (1 << 8))) != 0) {
            break;
          }
          mmm <<= 1;
        } while (true);
      } while ((mm <<= 3) < (1 << 9));
    }
    m = reflect(m);
  }

  uniq(res);
  return res;
}

set<tuple<ll, ll, ll, int, int>> bad;

ll put(int mask, int x, int y) {
  y -= __builtin_ctzll(mask);
  ll res = 0;

  for (int i = 0; i < 3; i++) {
    for (int j = 0; j < 3; j++) {
      if (mask & (1 << (3 * i + j))) {
        int xx = x + i;
        int yy = y + j;
        if (0 <= xx && xx < 8 && 0 <= yy && yy < 8) {
          res |= (1ull << (xx * 8 + yy));
        } else {
          return 0;
        }
      }
    }
  }
  return res;
}

