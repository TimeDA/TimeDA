int normalize_shift(int x) {
  while ((x & 7) == 0) x >>= 3;
  while ((x & (1 | (1 << 3) | (1 << 6))) == 0) x >>= 1;
  return x;
}

int rotate(int x) {
  int res = 0;
  for (int i = 0; i < 9; i++) {
    if (x & (1 << i)) {
      int a = i / 3;
      int b = i % 3;
      swap(a, b);
      a = 2 - a;
      int y = a * 3 + b;
      res |= (1 << y);
    }
  }
  return res;
}

int reflect(int x) {
  int res = 0;
  for (int i = 0; i < 9; i++) {
    if (x & (1 << i)) {
      int a = i / 3;
      int b = i % 3;
      a = 2 - a;
      int y = a * 3 + b;
      res |= (1 << y);
    }
  }
  return res;
}


int normalize(int x) {
  int v = normalize_shift(x);
  for (int j = 0; j < 2; j++) {
    for (int i = 0; i < 4; i++) {
      x = rotate(x);
      v = min(v, normalize_shift(x));
    }
    x = reflect(x);
  }
  return v;
}

void dfs(int &cur, int have, int x, int y) {
  if (cur & (1 << (3 * x + y))) return;
  cur |= (1 << (3 * x + y));
  const int dx[4] = {0, 1, 0, -1};
  const int dy[4] = {1, 0, -1, 0};
  for (int i = 0; i < 4; i++) {
    int xx = x + dx[i];
    int yy = y + dy[i];
    if (0 <= xx && xx < 3 && 0 <= yy && yy < 3) {
      if (have & (1 << (3 * xx + yy))) {
        dfs(cur, have, xx, yy);
      }
    }
  }
}

ll to_8x8(ll mask) {
  return (mask & 7) | (((mask >> 3) & 7) << 8) | (((mask >> 6) & 7) << 16);
}

